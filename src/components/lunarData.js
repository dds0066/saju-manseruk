// 유틸리티 함수들
export const lunarUtils = {
  /**
   * 특정 연도가 윤년인지 확인
   */
  isLeapYear(year) {
    return lunarMonthlyDays[year] && lunarMonthlyDays[year].length === 13;
  },

  /**
   * 특정 연도의 윤달 정보 반환
   */
  getLeapMonth(year) {
    if (!this.isLeapYear(year)) return null;
    return leapMonthInfo[year] || null;
  },

  /**
   * 특정 연도의 월별 일수 반환
   */
  getMonthDays(year, month) {
    const months = lunarMonthlyDays[year];
    if (!months || month < 1 || month > months.length) return 0;
    return months[month - 1];
  },

  /**
   * 특정 연도의 총 일수 계산
   */
  getTotalDaysInYear(year) {
    const months = lunarMonthlyDays[year];
    if (!months) return 0;
    return months.reduce((sum, days) => sum + days, 0);
  },

  /**
   * 음력 날짜를 양력으로 변환
   */
  lunarToSolar(lunarYear, lunarMonth, lunarDay, isLeapMonth = false) {
    const baseDate = lunarNewYearDates[lunarYear];
    const monthDays = lunarMonthlyDays[lunarYear];

    if (!baseDate || !monthDays) {
      // 데이터가 없는 경우 근사치 계산
      const estimatedBase = new Date(lunarYear, 1, 10);
      let totalDays = 0;
      
      // 기본 월별 일수로 계산 (29일/30일 교대)
      for (let i = 1; i < lunarMonth; i++) {
        totalDays += (i % 2 === 1) ? 29 : 30;
      }
      totalDays += lunarDay - 1;

      const resultDate = new Date(estimatedBase);
      resultDate.setDate(estimatedBase.getDate() + totalDays);
      
      return {
        year: resultDate.getFullYear(),
        month: resultDate.getMonth() + 1,
        day: resultDate.getDate(),
        isApproximate: true
      };
    }

    // 정확한 계산
    let totalDays = 0;
    const leapMonth = this.getLeapMonth(lunarYear);
    
    // 해당 월까지의 일수 계산
    for (let i = 1; i < lunarMonth; i++) {
      totalDays += monthDays[i - 1];
    }
    
    // 윤달 처리
    if (isLeapMonth && leapMonth === lunarMonth) {
      // 윤달인 경우, 평달 다음의 윤달
      totalDays += monthDays[lunarMonth - 1]; // 평달 일수 추가
      totalDays += lunarDay - 1; // 윤달 일수
    } else if (leapMonth && leapMonth < lunarMonth) {
      // 윤달이 지난 후의 월인 경우
      totalDays += monthDays[leapMonth]; // 윤달 일수 추가
      totalDays += lunarDay - 1;
    } else {
      // 일반적인 경우
      totalDays += lunarDay - 1;
    }

    const resultDate = new Date(baseDate);
    resultDate.setDate(baseDate.getDate() + totalDays);

    return {
      year: resultDate.getFullYear(),
      month: resultDate.getMonth() + 1,
      day: resultDate.getDate(),
      isApproximate: false
    };
  },

  /**
   * 양력 날짜를 음력으로 변환 (기본 구현)
   */
  solarToLunar(solarYear, solarMonth, solarDay) {
    // 이 함수는 복잡한 역계산이 필요하므로 기본 구현만 제공
    // 실제 사용시에는 더 정확한 알고리즘이 필요
    return {
      year: solarYear,
      month: solarMonth,
      day: solarDay,
      isLeapMonth: false,
      isApproximate: true
    };
  }
};